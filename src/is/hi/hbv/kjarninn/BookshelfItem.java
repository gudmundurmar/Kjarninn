package is.hi.hbv.kjarninn;

public class BookshelfItem {

    public int Id;
    public String IconFile;
    public String Title;
    public String Headline;
    public String Date;

    public BookshelfItem(int id, String iconFile, String title, String headline, String date) {

        Id = id;
        IconFile = iconFile;
        Title = title;
        Headline = headline;
        Date = date;

    }

}
