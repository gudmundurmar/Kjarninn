package is.hi.hbv.kjarninn;

/**
 * Create navbar item
 */
public class NavbarItem {

    public int Id;
    public String IconFile;
    public String Name;

    /**
     * Constructor
     * @param id
     * @param iconFile
     * @param name
     */
    public NavbarItem(int id, String iconFile, String name) {

        Id = id;
        IconFile = iconFile;
        Name = name;

    }

}
